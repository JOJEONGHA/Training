
public class SampleVO {
	int num;
	String sample;

	public SampleVO(int num, String sample) {
		this.num = num;
		this.sample = sample;
	}

	public int getNum() {
		return this.num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getSample() {
		return this.sample;
	}

	public void setSample(String sample) {
		this.sample = sample;
	}

}
